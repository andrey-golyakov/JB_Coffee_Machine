import java.util.*

fun main(args: Array<String>) {
    val scanner = Scanner(System.`in`)
    val str = scanner.nextLine()

    when (str) {
        "one" -> print("1")
        "two" -> print("2")
        "three" -> print("3")
        "four" -> print("4")
        "five" -> print("5")
        "six" -> print("6")
        "seven" -> print("7")
        "eight" -> print("8")
        "nine" -> print("9")
    }
}
